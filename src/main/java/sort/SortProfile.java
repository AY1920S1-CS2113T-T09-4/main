package sort;

public class SortProfile {
    private String alphaOrder;
    private String latestDatesOrder;
    private String highestRatingOrder;

    public String getAlphaOrder() {
        return alphaOrder;
    }

    public void setAlphaOrder(String alphaOrder) {
        this.alphaOrder = alphaOrder;
    }

    public String getLatestDatesOrder() {
        return latestDatesOrder;
    }

    public void setLatestDatesOrder(String latestDatesOrder) {
        this.latestDatesOrder = latestDatesOrder;
    }

    public String getHighestRatingOrder() {
        return highestRatingOrder;
    }

    public void setHighestRatingOrder(String highestRatingOrder) {
        this.highestRatingOrder = highestRatingOrder;
    }
}


